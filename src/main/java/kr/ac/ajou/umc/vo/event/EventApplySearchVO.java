package kr.ac.ajou.umc.vo.event;

import io.swagger.annotations.ApiModelProperty;

public class EventApplySearchVO {

    @ApiModelProperty(notes = "이벤트 id")
    private long eventId;

    @ApiModelProperty(notes = "이벤트 title")
    private String eventTitle;

    @ApiModelProperty(notes = "이벤트 이미지 링크")
    private String imageLink;

    @ApiModelProperty(notes = "지원자 트럭 유저 id")
    private long userId;

    @ApiModelProperty(notes = "지원자 이름")
    private String name;

    @ApiModelProperty(notes = "지원자 핸드폰 번호")
    private String phoneNum;

    @ApiModelProperty(notes = "지원자 트럭 id")
    private long truckId;

    @ApiModelProperty(notes = "지원자 트럭 이름")
    private String truckName;


    public long getEventId() {
        return eventId;
    }

    public void setEventId(long eventId) {
        this.eventId = eventId;
    }

    public String getEventTitle() {
        return eventTitle;
    }

    public void setEventTitle(String eventTitle) {
        this.eventTitle = eventTitle;
    }

    public String getImageLink() {
        return imageLink;
    }

    public void setImageLink(String imageLink) {
        this.imageLink = imageLink;
    }

    public long getUserId() {
        return userId;
    }

    public void setUserId(long userId) {
        this.userId = userId;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getPhoneNum() {
        return phoneNum;
    }

    public void setPhoneNum(String phoneNum) {
        this.phoneNum = phoneNum;
    }

    public long getTruckId() {
        return truckId;
    }

    public void setTruckId(long truckId) {
        this.truckId = truckId;
    }

    public String getTruckName() {
        return truckName;
    }

    public void setTruckName(String truckName) {
        this.truckName = truckName;
    }
}
