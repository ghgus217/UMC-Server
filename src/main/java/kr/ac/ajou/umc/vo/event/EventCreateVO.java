package kr.ac.ajou.umc.vo.event;

import io.swagger.annotations.ApiModelProperty;

import java.util.Date;

public class EventCreateVO {

    @ApiModelProperty(notes = "이벤트 관계자 id")
    private long userId;

    @ApiModelProperty(notes = "이벤트 타이틀")
    private String eventTitle;

    @ApiModelProperty(notes = "이벤트 위도")
    private double lat;

    @ApiModelProperty(notes = "이벤트 경도")
    private double lon;

    @ApiModelProperty(notes = "이벤트 세부사항")
    private String description;

    @ApiModelProperty(notes = "이벤트 이미지 경로")
    private String imageLink;

    @ApiModelProperty(notes = "이벤트 시작시간")
    private Date startTime;

    @ApiModelProperty(notes = "이벤트 종료시간")
    private Date endTime;

    public long getUserId() {
        return userId;
    }

    public void setUserId(long userId) {
        this.userId = userId;
    }

    public String getEventTitle() {
        return eventTitle;
    }

    public void setEventTitle(String eventTitle) {
        this.eventTitle = eventTitle;
    }

    public double getLat() {
        return lat;
    }

    public void setLat(double lat) {
        this.lat = lat;
    }

    public double getLon() {
        return lon;
    }

    public void setLon(double lon) {
        this.lon = lon;
    }

    public String getDescription() {
        return description;
    }

    public void setDescription(String description) {
        this.description = description;
    }

    public String getImageLink() {
        return imageLink;
    }

    public void setImageLink(String imageLink) {
        this.imageLink = imageLink;
    }

    public Date getStartTime() {
        return startTime;
    }

    public void setStartTime(Date startTime) {
        this.startTime = startTime;
    }

    public Date getEndTime() {
        return endTime;
    }

    public void setEndTime(Date endTime) {
        this.endTime = endTime;
    }
}
